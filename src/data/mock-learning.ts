export function getLessonById(id: string) {
  return lessons.find((lesson) => lesson.id === id) ?? lessons[0];
}

export function getCharacterById(id: string) {
  return characters.find((character) => character.id === id) ?? characters[0];
}

export function getNoteById(id: string) {
  return notes.find((note) => note.id === id) ?? notes[0];
}

export function getLessonContent(lesson: Lesson) {
  return {
    vocabulary: vocabulary.filter((item) => lesson.vocabularyIds.includes(item.id)),
    grammar: grammarPoints.filter((item) => lesson.grammarIds.includes(item.id)),
    characters: characters.filter((item) => lesson.characterIds.includes(item.id)),
    sentences: exampleSentences.filter((sentence) =>
      lesson.vocabularyIds.some((id) => sentence.vocabularyIds.includes(id))
    ),
  };
}
